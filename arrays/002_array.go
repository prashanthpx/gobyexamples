package main

import (
	"fmt"
)

func main() {
	a := [3]int{12, 78, 50} // short hand declaration to create array
	fmt.Println(a)
}

/*
Output
[12 78 50]
*/

/*
Code Explanation:
- Purpose: Show array literal initialization
- a := [3]int{12, 78, 50} initializes all 3 elements explicitly
- Printing the array shows the values in order
*/
