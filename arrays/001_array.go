package main

import (
	"fmt"
)

func main() {
	var a [3]int //int array with length 3
	fmt.Println(a)
}

/*
Output
[0 0 0]
*/

/*
Code Explanation:
- Purpose: Show default zero values of an int array
- var a [3]int declares a fixed-length array of 3 ints, zero-initialized
- Printing the array shows all elements as 0
*/
